    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:

        try{
            String name = this.jTextField1.getText();
            String email = this.jTextField2.getText();
            String address = this.jTextField3.getText();
            String cnpj = this.jFormattedTextField3.getText();
            String companyType = this.jComboBox1.getSelectedItem().toString();
            String conpanyDescription = this.jTextPane1.getText();
            String contact= this.jFormattedTextField4.getText();
            int nb = 0;
            try {
                nb = Integer.parseInt(cnpj);

            } catch (NumberFormatException e) {
                System.out.println("Numero com formato errado!");
            }
            
            if(name == null || email == null || contact == null || companyType == null || address == null){
                throw new Exception();
            }

            LegalPerson ass = new LegalPerson( nb, companyType, conpanyDescription, address, name, email, contact);
            ctrl.append(ass);
            this.updateTable();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Algo errado com os dados inseridos.","Dados inconsistentes", JOptionPane.WARNING_MESSAGE);
            return;
        }
    }//GEN-LAST:event_jButton4ActionPerformed

