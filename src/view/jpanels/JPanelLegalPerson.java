        LegalPerson s  = this.lpLst.get(selected);

        if(s == null){
            return;
        }

        List<LegalPerson> lstSelected = new ArrayList<>();
        lstSelected.add(s);

        this.jList1.setModel(new GeneralComboboxModel<LegalPerson>().getComboBoxModelUsingDescription(lstSelected));
