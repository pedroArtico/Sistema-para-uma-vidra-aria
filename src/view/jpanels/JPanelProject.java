    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            String title = this.jTextField1.getText();
            String description =this.jTextField2.getText();
            String file = this.jTextField3.getText();
            
            if(title == null || description == null || file == null){
                throw new Exception();
            }
            Project p = new Project(title, description, file, null);
            ctrl.append(p);
            this.updateTable();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Algo errado com os dados inseridos.","Dados inconsistentes", JOptionPane.WARNING_MESSAGE);
            return;
        }                                  

    }//GEN-LAST:event_jButton2ActionPerformed

