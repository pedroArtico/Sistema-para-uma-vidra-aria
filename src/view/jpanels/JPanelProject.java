    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        for(int i = 0; i < this.projTb.getRowCount()-1; i++){
            Project newProject = this.projTb.getObjectAt(i);

            String description = this.descriptions.get(i);
            if(!newProject.describe().equals(description)){
                this.ctrl.update(newProject, description);
            }
        }
        this.projectMp = ctrl.getProject();
        this.projectLst = new ArrayList<>(projectMp.values());
        this.updateDescriptions();
        JOptionPane.showMessageDialog(this,"Dados atualizados.","Sucesso", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_jButton1ActionPerformed

